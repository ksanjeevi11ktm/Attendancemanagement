package Attendancemanagement.classes;

public class subject {
	int subjectid;
	String subjectname;
	String updatedsubjectname;
	
	public int getSubjectid() {
		return subjectid;
	}
	public void setSubjectid(int subjectid) {
		this.subjectid = subjectid;
	}
	public String getSubjectname() {
		return subjectname;
	}
	public void setSubjectname(String subjectname) {
		this.subjectname = subjectname;
	}
	public String getUpdatedsubjectname() {
		return updatedsubjectname;
	}
	public void setUpdatedsubjectname(String updatedsubjectname) {
		this.updatedsubjectname = updatedsubjectname;
	}
	

}
